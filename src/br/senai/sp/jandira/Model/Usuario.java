package br.senai.sp.jandira.Model;

public class Usuario {
    String nome, email, endereco, telefone;

    public Usuario() {
    }

    public String getNome() {
        return nome;
    }

    public String getEmail() {
        return email;
    }

    public String getEndereco() {
        return endereco;
    }

    public String getTelefone() {
        return telefone;
    }
}
